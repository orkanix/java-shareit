CREATE TABLE IF NOT EXISTS users (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS requests (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id BIGINT NOT NULL CONSTRAINT requests_user_fkey REFERENCES users(id),
    description VARCHAR(255),
    created TIMESTAMP NOT NULL
);

CREATE TABLE IF NOT EXISTS items (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(255),
    available BOOLEAN NOT NULL,
    owner_id BIGINT NOT NULL CONSTRAINT items_owner_fkey REFERENCES users(id),
    request_id BIGINT CONSTRAINT items_request_fkey REFERENCES requests(id)
);

CREATE TABLE IF NOT EXISTS bookings (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    item_id BIGINT NOT NULL CONSTRAINT bookings_item_fkey REFERENCES items(id),
    start_date TIMESTAMP NOT NULL,
    end_date TIMESTAMP NOT NULL,
    user_id BIGINT NOT NULL CONSTRAINT bookings_user_fkey REFERENCES users(id),
    booked BOOLEAN NOT NULL,
    status VARCHAR(127) NOT NULL DEFAULT 'WAITING'
);

CREATE TABLE IF NOT EXISTS comments (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    text VARCHAR(255) NOT NULL,
    item_id BIGINT NOT NULL CONSTRAINT comments_item_fkey REFERENCES items(id),
    author_id BIGINT NOT NULL CONSTRAINT comments_user_fkey REFERENCES users(id),
    created TIMESTAMP NOT NULL
);